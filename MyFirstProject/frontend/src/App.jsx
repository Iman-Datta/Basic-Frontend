import React, { useEffect, useState } from "react";
function App() {
  const [message, setMessage] = useState("");
  useEffect(() => {
    fetch("http://localhost:5000/")
      .then((res) => res.text())
      .then((data) => setMessage(data));
  }, []);
  return <div style={{ padding: "20px", fontSize: "24px" }}>{message}</div>;
}
export default App;